type AppendEntriesArgs struct {
	Term         int
	LeaderID     int
	PrevLogIndex int
	PrevLogTerm  int
	Entries      []LogEntry
	LeaderCommit int
}

type AppendEntriesReply struct {
	Term    int
	Success bool
	XTerm   int
	XIndex  int
	XLen    int
	//  lab页面上的快速回退做法
	//  XTerm:  term in the conflicting entry (if any)
	//  XIndex: index of first entry with that term (if any)
	//  XLen:   log length
}
